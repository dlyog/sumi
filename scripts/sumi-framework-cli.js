#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");

function fail(message) {
  process.stderr.write(`sumi-framework: ${message}\n`);
  process.exitCode = 1;
}

function option(name, fallback = "") {
  const index = process.argv.indexOf(name);
  return index >= 0 && process.argv[index + 1] ? process.argv[index + 1] : fallback;
}

function validateRegistry(registry) {
  if (!registry || typeof registry !== "object") throw new Error("registry must be a JSON object");
  if (!registry.screen_id || typeof registry.screen_id !== "string") throw new Error("screen_id is required");
  if (!Array.isArray(registry.actions) || registry.actions.some((entry) => !entry || typeof entry.id !== "string")) {
    throw new Error("actions must be an array with stable id values");
  }
  if (!Array.isArray(registry.terms)) throw new Error("terms must be an array");
  Object.entries(registry.screens || {}).forEach(([screenId, screen]) => {
    if (!Array.isArray(screen.allowed_actions)) throw new Error(`screens.${screenId}.allowed_actions must be an array`);
    Object.entries(screen.action_bindings || {}).forEach(([actionId, binding]) => {
      if (!screen.allowed_actions.includes(actionId)) throw new Error(`screens.${screenId}.action_bindings.${actionId} is not allowed`);
      if (!binding || typeof binding.confirmation !== "string") throw new Error(`screens.${screenId}.action_bindings.${actionId}.confirmation is required`);
    });
  });
}

function writeNew(destination, content) {
  if (fs.existsSync(destination)) throw new Error(`${destination} already exists`);
  fs.writeFileSync(destination, content, "utf8");
}

function init() {
  const screenId = option("--screen");
  const title = option("--title", screenId);
  const output = path.resolve(option("--out", path.join(process.cwd(), "sumi")));
  if (!/^[a-z0-9][a-z0-9-]*$/.test(screenId)) throw new Error("--screen must use lowercase letters, digits, and hyphens");
  fs.mkdirSync(output, { recursive: true });
  const registry = {
    version: "1.0.0",
    screen_id: screenId,
    title,
    voice: { intro_barge_in_grace_ms: 1200, fallback: "llm-and-realtime-tts" },
    actions: [
      { id: "skip_intro", description: "Skip the current introduction.", aliases: ["skip intro"] },
      { id: "stop_conversation", description: "Stop Sumi safely.", aliases: ["stop sumi"] },
      { id: "explain_screen", description: "Explain the current screen.", aliases: ["explain this screen"] },
      { id: "answer_question", description: "Answer without changing the screen.", aliases: [] },
    ],
    terms: [],
    screens: { [screenId]: { title, description: `Explain ${title} here.`, audio_id: "", allowed_actions: ["skip_intro", "stop_conversation", "explain_screen", "answer_question"] } },
  };
  const config = {
    version: "1.0.0",
    screen_id: screenId,
    providers: {
      speech_to_text: { url_env: "SUMI_STT_URL" },
      llm: { url_env: "SUMI_LLM_URL" },
      text_to_speech: { url_env: "SUMI_TTS_URL" },
    },
  };
  const adapter = `// Generated by sumi-framework. Keep provider secrets on your server.\n` +
`const SUMI_STT_URL = window.SUMI_STT_URL || "/api/sumi/stt";\n` +
`const SUMI_LLM_URL = window.SUMI_LLM_URL || "/api/sumi/llm";\n` +
`const SUMI_TTS_URL = window.SUMI_TTS_URL || "/api/sumi/tts";\n\n` +
`const sumi = new window.SumiFramework({\n` +
`  screenId: ${JSON.stringify(screenId)},\n` +
`  registryUrl: ${JSON.stringify(`./${screenId}.registry.json`)},\n` +
`  uiActions: window.SumiUIActions,\n` +
`  providers: {\n` +
`    stt: async (audio) => { const body = new FormData(); body.append("audio", audio); const result = await fetch(SUMI_STT_URL, { method: "POST", body }).then((response) => response.json()); return result.text || result.transcription; },\n` +
`    llm: async (turn) => fetch(SUMI_LLM_URL, { method: "POST", headers: { "Content-Type": "application/json" }, body: JSON.stringify(turn) }).then((response) => response.json()),\n` +
`    tts: async (text) => fetch(SUMI_TTS_URL, { method: "POST", headers: { "Content-Type": "application/json" }, body: JSON.stringify({ text }) }).then((response) => response.blob()),\n` +
`  },\n` +
`});\n\nawait sumi.initialize();\n`;
  writeNew(path.join(output, "sumi.config.json"), `${JSON.stringify(config, null, 2)}\n`);
  writeNew(path.join(output, `${screenId}.registry.json`), `${JSON.stringify(registry, null, 2)}\n`);
  writeNew(path.join(output, `${screenId}.adapter.js`), adapter);
  process.stdout.write(`Created Sumi integration for ${title} in ${output}\n`);
}

function validate() {
  const destination = process.argv[3];
  if (!destination) throw new Error("validate requires a registry path");
  validateRegistry(JSON.parse(fs.readFileSync(path.resolve(destination), "utf8")));
  process.stdout.write(`Sumi registry is valid: ${path.resolve(destination)}\n`);
}

function help() {
  process.stdout.write("Usage:\n  sumi-framework init --screen <id> --title <title> [--out <directory>]\n  sumi-framework validate <registry.json>\n");
}

try {
  const command = process.argv[2];
  if (command === "init") init();
  else if (command === "validate") validate();
  else if (command === "help" || command === "--help" || !command) help();
  else fail(`unknown command: ${command}`);
} catch (error) {
  fail(error.message);
}
